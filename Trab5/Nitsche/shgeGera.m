function [shge] = shgeGera(nen,nint)
  pr(1) = -1.;
  pr(2) = 1.;
  %monta as funções de base
  for l = 1:2
    t = pr(l);
    switch (nen)
      %grau 1
      case 2
        shge(1,1,l) = 0.5*(1.0 - t);
        shge(1,2,l) = 0.5*(1.0 + t);
        shge(2,1,l) = -1.0/2.0;
        shge(2,2,l) = 1.0/2.0;
      %grau 2
      case 3
        shge(1,1,l) = (t - 1.0)*t/2.0;
        shge(1,2,l) = -(t - 1.0)*(t + 1.0);
        shge(1,3,l) = (t + 1.0)*t/2.0;
        shge(2,1,l) = (2.0*t-1.0)/2.0;
        shge(2,2,l) = -2.0*t;
        shge(2,3,l) = (2.0*t+1.0)/2.0;
      %grau 3
      case 4
        shge(1,1,l) = -(9.0/16.0)*(t+1.0/3.0)*(t-1.0/3.0)*(t-1.0);
        shge(1,2,l) = (27.0/16.0)*(t+1.0)*(t-1.0/3.0)*(t-1.0);
        shge(1,3,l) = -(27.0/16.0)*(t+1.0)*(t+1.0/3.0)*(t-1.0);
        shge(1,4,l) = (9.0/16.0)*(t+1.0/3.0)*(t-1.0/3.0)*(t+1.0);
        shge(2,1,l) = -(3.0*t^2 - 2*t - 1)/16.0;
        shge(2,2,l) = (3.0*t^2 - 2*t - 1)*9.0/16.0;
        shge(2,3,l) = -(3.0*t^2 - 2*t - 1)*9.0/16.0;
        shge(2,4,l) = (3.0*t^2 - 2*t - 1)/16.0;
      %grau 4
      case 5
        shge(1,1,l) = (2.0/3.0)*(t+(1.0/2.0))*t*(t-(1.0/2.0))*(t-1.0);
        shge(1,2,l) = -(8.0/3.0)*(t-(1.0/2.0))*t*(t-1.0)*(t+1.0);
        shge(1,3,l) = 4.0*(t+1.0)*(t+(1.0/2.0))*(t-(1.0/2.0))*(t-1.0);
        shge(1,4,l) = -(8.0/3.0)*(t+(1.0/2.0))*t*(t-1.0)*(t+1.0);
        shge(1,5,l) = (2.0/3.0)*(t+(1.0/2.0))*t*(t-(1.0/2.0))*(t+1.0);
        shge(2,1,l) = (16.0*t^3 - 12.0*t^2 - 2*t + 1.0)/6.0;
        shge(2,2,l) = -(8.0*t^3 - 3.0*t^2 - 4.0*t + 1.0)*4.0/3.0;
        shge(2,3,l) = 16.0*t^3 - 10.0*t;
        shge(2,4,l) = -(8.0*t^3 + 3.0*t^2 - 4.0*t - 1.0)*4.0/3.0;
        shge(2,5,l) = (16.0*t^3 + 12.0*t^2 - 2*t - 1.0)/6.0;
    endswitch
  endfor 
endfunction
